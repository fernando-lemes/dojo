/**
 * Classe com a posicao (x, y) da bomba no campo
 */
public class Posicao {
    int x;
    int y;

    public Posicao(int y, int x) {
        this.y = y;
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}
